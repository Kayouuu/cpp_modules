#include "Zombie.hpp"

Zombie::Zombie(std::string name)
{
	this->name = name;
	std::cout << this->name << " has returned from the lambs" << std::endl;
	return ;
}

Zombie::~Zombie(void)
{
	std::cout << this->name << " die a second time" << std::endl;
	return ;
}

void	Zombie::announce(void)
{
	std::cout << this->name << ": BraiiiiiiinnnzzzZ..." << std::endl;
}
